
public class Direction 
{
	private int y;
	private int x;
	private String direction;
	
	public Direction(int x, int y, String direction)
	{
		
		this.x=x;
		this.y=y;
		this.direction = direction;
	}
	
	public int getX()
	{
		return x;
	}
	
	public int getY()
	{
		return y;
	}
	
	public String getDirection()
	{
		return direction;
	}
	
	
	
	
}